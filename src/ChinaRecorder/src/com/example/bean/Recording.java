package com.example.bean;

import java.util.Date;

public class Recording {
	private int rid;
	private String rname;
	private Date rdate;
	private String rpic;
	private String rurl;
	private float rsize;
	private String rduration;
	private String rformat;
	
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public String getRname() {
		return rname;
	}
	public void setRname(String rname) {
		this.rname = rname;
	}
	public Date getRdate() {
		return rdate;
	}
	public void setRdate(Date rdate) {
		this.rdate = rdate;
	}
	public String getRpic() {
		return rpic;
	}
	public void setRpic(String rpic) {
		this.rpic = rpic;
	}
	public String getRurl() {
		return rurl;
	}
	public void setRurl(String rurl) {
		this.rurl = rurl;
	}
	public float getRsize() {
		return rsize;
	}
	public void setRsize(float rsize) {
		this.rsize = rsize;
	}
	public String getRduration() {
		return rduration;
	}
	public void setRduration(String rduration) {
		this.rduration = rduration;
	}
	public String getRformat() {
		return rformat;
	}
	public void setRformat(String rformat) {
		this.rformat = rformat;
	}
	
	
	
	
}
